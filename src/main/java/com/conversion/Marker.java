package com.conversion;

public class Marker {

    public String freq;
    public String offset;
    public String pl;
    public String location;
    public String landmark;
    public String latitude;
    public String longtitude;
    public String call;
    public String stateId;
    public String id;
    public String type;

    public Marker() {
    }

    public String getFreq() {
        return freq;
    }

    public void setFreq(final String freq) {
        this.freq = freq;
    }

    public String getOffset() {
        return offset;
    }

    public void setOffset(final String offset) {
        this.offset = offset;
    }

    public String getPl() {
        return pl;
    }

    public void setPl(final String pl) {
        this.pl = pl;
    }

    public String getLocation() {
        return location;
    }

    public void setLocation(final String location) {
        this.location = location;
    }

    public String getLandmark() {
        return landmark;
    }

    public void setLandmark(final String landmark) {
        this.landmark = landmark;
    }

    public String getLatitude() {
        return latitude;
    }

    public void setLatitude(final String latitude) {
        this.latitude = latitude;
    }

    public String getLongtitude() {
        return longtitude;
    }

    public void setLongtitude(final String longtitude) {
        this.longtitude = longtitude;
    }

    public String getCall() {
        return call;
    }

    public void setCall(final String call) {
        this.call = call;
    }

    public String getStateId() {
        return stateId;
    }

    public void setStateId(final String stateId) {
        this.stateId = stateId;
    }

    public String getId() {
        return id;
    }

    public void setId(final String id) {
        this.id = id;
    }

    public String getType() {
        return type;
    }

    public void setType(final String type) {
        this.type = type;
    }
}
