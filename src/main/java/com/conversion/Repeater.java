package com.conversion;

public class Repeater {

    public String id;
    public String name;
    public String frequencyOut;
    public String toneIn;
    public String toneOut;
    public Location location;
    public String radius;
    public String haat;
    public String status;


    public String getId() {
        return id;
    }

    public void setId(final String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(final String name) {
        this.name = name;
    }

    public String getFrequencyOut() {
        return frequencyOut;
    }

    public void setFrequencyOut(final String frequencyOut) {
        this.frequencyOut = frequencyOut;
    }

    public String getToneIn() {
        return toneIn;
    }

    public void setToneIn(final String toneIn) {
        this.toneIn = toneIn;
    }

    public String getToneOut() {
        return toneOut;
    }

    public void setToneOut(final String toneOut) {
        this.toneOut = toneOut;
    }

    public Location getLocation() {
        return location;
    }

    public void setLocation(final Location location) {
        this.location = location;
    }

    public String getRadius() {
        return radius;
    }

    public void setRadius(final String radius) {
        this.radius = radius;
    }

    public String getHaat() {
        return haat;
    }

    public void setHaat(final String haat) {
        this.haat = haat;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(final String status) {
        this.status = status;
    }
}
